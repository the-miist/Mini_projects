def addition(num_1,num_2):
    res_add = num_1 + num_2
    return res_add

def subtraction(num_1,num_2):
    res_sub = num_1 - num_2
    return res_sub

def muliplication(num_1,num_2):
    res_mul = num_1 * num_2
    return res_mul

def division(num_1,num_2):
    res_div = num_1 / num_2
    return res_div
